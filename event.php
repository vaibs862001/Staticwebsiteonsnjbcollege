                                <h3>Infrastructure For Campus</h3>
                                <p>
                                    

                                    Chandwad is the Historical place of the Maharashtra surrounded by lush green mountain range. Presence of “Renuka devi” temple and “Shri Ganesh Mandir” gives it mythological importance. Being in close vicinity to Nashik is a good advantage for Chandwad. From years, Nashik has been recognized as a major industrial, social, political, and cultural centre. The city hosts a number of renowned industries like Thermal Power Plant of Eklahara, Mahindra & Mahindra, Mico (Bosch), CEAT, Glaxo, Crompton and Greaves and many more. Defence Ministry’s Fighter Plane Production Unit (MIG and SUKOI) HAL at Ozar also serves as an important feature of Nashik.
                                </p>
                                <p>
                                    The presence of one of the 12 Jyotirlings at Trimbakeshwar, Kalaram Temple, Pandav Leni, Saptashrungi Gadh (Vani) gives mythological importance to Nashik. Other interesting places at Nashik includes: Phalke Smarak, Kalsubai (highest mountain peaks of Western Ghats), Nandur-Madhameshwar (one of the important water habitats).
                                </p>
                                <h4>Infrastructure For Library</h4>
